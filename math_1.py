import math

print(5/2) # Floating-point division
print(5//2) # Floor division
print(-3//2) # Floor division with negative number
print(int(-3/2)) # Converting float division to int

print(10 % 3) # Modulus operator
print(-10 % 3) # Modulus with negative number

print(math.fmod(-10, 3)) # Using math.fmod for modulus
print(math.floor(-10 / 3)) # Using math.floor for floor division
print(math.ceil(10 / 3)) # Using math.ceil for ceiling division
print(math.sqrt(16)) # Square root
print(math.pow(2, 3)) # Power function